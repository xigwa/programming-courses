//        git add <file>: Додає файли до індексу (staging area) для подальшого коміту.
//        git commit -m "Повідомлення коміту": Створює коміт з доданими до індексу файлами та пов'язаною з ними повідомленням.
//        git status: Показує стан робочого каталогу та індексу.
//        git pull: Витягує зміни з віддаленого репозиторію і об'єднує їх з поточною гілкою.
