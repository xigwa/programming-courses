//        git branch: Показує список локальних гілок, а також допомагає створювати, видаляти і перемикатися між ними.
//        git checkout <branch>: Перемикається на вказану гілку.
//        git merge <branch>: Об'єднує вказану гілку з поточною.
//        git fetch: Витягує зміни з віддаленого репозиторію, але не об'єднує їх з поточною гілкою.
//        git remote: Показує список віддалених репозиторіїв та дозволяє керувати ними.







